package com.order.kamlesh;

public class Images {

	private String small;
	private String medium;
	private String big;
	
	public Images() {
		
	}
	
	public Images(String small, String medium, String big) {
		super();
		this.small = small;
		this.medium = medium;
		this.big = big;
	}
	public String getSmall() {
		return small;
	}
	public void setSmall(String small) {
		this.small = small;
	}
	public String getMedium() {
		return medium;
	}
	public void setMedium(String medium) {
		this.medium = medium;
	}
	public String getBig() {
		return big;
	}
	public void setBig(String big) {
		this.big = big;
	}
	
	
}
